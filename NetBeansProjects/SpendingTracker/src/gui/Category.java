   private void getEntries() {
        try{
            javax.swing.table.DefaultTableModel dtm=
            (javax.swing.table.DefaultTableModel) table.getModel();
            int rc= dtm.getRowCount();     //these three lines code to empty the table while feeding from database repeatedly
            while(rc--!=0) {
                dtm.removeRow(0);
            }
            
           ResultSet rs= db.DbConnect.st.executeQuery(
                   "select * from category_info");
           int sno=0;
           while(rs.next()) {
               String category = rs.getString("category");
               
//               Object o[] = {++sno,category};     //this is with the help of object array
//               dtm.addRow(o);

              java.util.Vector row= new java.util.Vector();      //with the help of collection 
              row.add(++sno);
              row.add(category);
              dtm.addRow(row);
           }
           
        }catch(Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
            
        }
    }

