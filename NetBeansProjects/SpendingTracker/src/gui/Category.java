    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            String category= t.getText();
            if(!category.equals("")){
               db.DbConnect.st.executeUpdate
                 ("insert into category_info values('"+category+"')");
               JOptionPane.showMessageDialog(null, 
                   "Category Added Successfully!");
               getEntries();
            }else{
                JOptionPane.showMessageDialog(null, 
                        "Please enter the Category First!");
            }
          
         
        }catch(SQLIntegrityConstraintViolationException ex) {
        JOptionPane.showMessageDialog(null, "Category Already exist!");
        }catch(Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int ri=table.getSelectedRow();
        if(ri!=-1) {
        int r= JOptionPane.showConfirmDialog(null,
        "Do you really wanna delete?" , "Deletion Confirmation" ,
        JOptionPane.YES_NO_OPTION);
           if(r==JOptionPane.YES_OPTION) {
                
            String category=(String)table.getValueAt(ri, 1);   //deleted value of table in category
            try{
                db.DbConnect.st.executeUpdate
        ("delete from category_info where category='"+category+"'");
                JOptionPane.showMessageDialog(null, 
                        "Category Deleted Successfully!");
                    getEntries();
          
          
                     }catch(Exception ex) {
                        JOptionPane.showMessageDialog(null, ex);
                  }
                 }
        }
    
                
    
    
    }//GEN-LAST:event_jButton2ActionPerformed

